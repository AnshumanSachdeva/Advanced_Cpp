/*
Static variables:
    Static variables are initialized to 0, before the first object is created
    Only one copy of the static vriable will exist for the whole program
    All the object will share that variable
    It will remain in the memory till the end of the program
    
Static Function:
    A static function may be called by itself without depending uponany object
    To access the static fn we use, 
        class name :: staticfunction();
*/