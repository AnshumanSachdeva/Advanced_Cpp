/* 
Constructor: 
    A constructor is a member funtion
    It has the same name as the class name 
    Constructor cannot return values
    It is automatically called when an object is created
    
Types of Constructor:
    1. Parameterized Constructor
    2. Default Constructor
    3. Copy Constructor
    
Destructor:
    Destructors are used to deallocate memory
    They are called when the object is destroyed
    A destructor takes no arguments and has no return arguments
    A tilde sign (~) is used just befor the name of constructor , and it forms the destructor*/